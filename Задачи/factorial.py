# 2. Почему в цикле используется промежуток (1, n + 1)? Что это значит?


# Здесь та же самая программа, но с парой промежуточных действий
# Эти действия выводят на экран информацию, которая поможет разобраться с тем, что происходит

m = int(input('Введите число, для которого нужно вычислить факториал: '))
fact = 1

for i in range (1, m + 1):
    print('====== ИТЕРАЦИЯ №', i, ' началась ======')
    print('Переменная в начале итерации', fact)
    fact = fact * i
    print('Переменная в конце итерации', fact)
    print('====== ИТЕРАЦИЯ №', i, ' закончилась ======')

print('Факториал числа ', m, ' = ', fact)